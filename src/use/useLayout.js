/**
 * Медиа-запрос, определяющий, когда ширина экрана будет меньше либо равна 926px
 */
export const mediaQueryMaxWidth926 = window.matchMedia('(max-width: 926px)');

/**
 * Медиа-запрос, определяющий, когда ширина экрана будет больше либо равна 927px
 */
export const mediaQueryMinWidth927 = window.matchMedia('(min-width: 927px)');